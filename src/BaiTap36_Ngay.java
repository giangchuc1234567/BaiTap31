public class BaiTap36_Ngay {
    private int Ngay,Thang,Nam;
    public BaiTap36_Ngay(int ngay, int thang, int nam ){
            this.Ngay = ngay;
            this.Thang = thang;
            this.Nam = nam;
    }

    public int getNgay() {
        return Ngay;
    }

    public void setNgay(int ngay) {
        Ngay = ngay;
    }

    public int getThang() {
        return Thang;
    }

    public void setThang(int thang) {
        Thang = thang;
    }

    public int getNam() {
        return Nam;
    }

    public void setNam(int nam) {
        Nam = nam;
    }
}
